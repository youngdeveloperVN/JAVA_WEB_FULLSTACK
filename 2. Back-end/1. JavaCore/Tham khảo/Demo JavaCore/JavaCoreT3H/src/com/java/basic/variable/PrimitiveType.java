package com.java.basic.variable;

public class PrimitiveType {
	public static void main(String[] args) {
		int a = 5;
		int b = a;
		System.out.println(a);
		System.out.println(b);
		System.out.println("////----------------------");
		a  = 8;
		System.out.println(a);
		System.out.println(b);
		
		
	}
}
